#include <unistd.h>

int		main(int ac, char **av)
{
	int		i;
	int		j;

	i = 0;
	if (ac != 2)
	{
		write(1, "\n", 1);
		return (0);
	}

	while (av[1][i] != '\0')
		i++;
	i--;

	while (i >= 0 && (av[1][i] == ' ' || av[1][i] == '\t'))
		i--;
	j = i;

	while (i >= 0 && (av[1][i] != ' ' || av[1][i] != '\t'))
	{
		if (av[1][i - 1] == ' ' || av[1][i - 1] == '\t')
			break ;
		i--;
	}
	while (i >= 0 && i <= j)
	{
		write(1, &av[1][i], 1);
		i++;
	}
	write(1, "\n", 1);
	return (0);
}
